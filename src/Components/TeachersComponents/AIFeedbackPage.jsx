import React, { useState, useEffect } from 'react';
import { useParams } from 'react-router-dom';
import { Container, Card, Alert, Spinner } from 'react-bootstrap';

const AIFeedbackPage = () => {
  const { examId } = useParams();
  const [feedbackText, setFeedbackText] = useState('');
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  useEffect(() => {
  const fetchData = async () => {
    try {
      setLoading(true);
      setError(null);

      const { data: questions, error: qErr } = await supabase
        .from('mcq_questions')
        .select('*')
        .eq('exam_id', examId);
      if (qErr) throw qErr;

      const { data: submissions, error: sErr } = await supabase
        .from('exam_submission')
        .select('*')
        .eq('exam_id', examId);
      if (sErr) throw sErr;

      // Simulate AI call
      setFeedbackText(`Fetched ${questions.length} questions and ${submissions.length} submissions.`);
    } catch (err) {
      setError(err.message);
    } finally {
      setLoading(false);
    }
  };

  fetchData();
}, [examId]);

  if (loading) {
    return (
      <div className="text-center my-5">
        <Spinner animation="border" />
        <p>Generating AI feedback...</p>
      </div>
    );
  }

  if (error) {
    return (
      <Container className="mt-4">
        <Alert variant="danger">
          <strong>Error:</strong> {error}
        </Alert>
      </Container>
    );
  }

  return (
    <Container className="mt-4">
      <Card className="shadow-sm mb-4">
        <Card.Header className="bg-primary text-white">
          <h4>AI-Generated Teaching Feedback</h4>
          <span className="small">Exam ID: {examId}</span>
        </Card.Header>
        <Card.Body>
          <pre style={{ whiteSpace: 'pre-wrap', fontFamily: 'inherit' }}>
            {feedbackText}
          </pre>
        </Card.Body>
      </Card>
      <Alert variant="info">
        <i className="bi bi-robot"></i> Feedback is generated by AI.
      </Alert>
    </Container>
  );
};

export default AIFeedbackPage;
